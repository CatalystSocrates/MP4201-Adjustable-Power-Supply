// -------------------------- 串口1中断接收回调函数（HAL库核心） --------------------------
// 每次接收1字节数据后，HAL库自动调用此函数
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {
    if (huart->Instance == USART1) {  // 确认是串口1
        // 1. 将接收的数据写入环形缓冲区
        UART_RingBuffer_Write(uart1_ring_buf, recv_data);
        // 2. 重新开启中断接收（HAL库中断接收需手动重启）
        HAL_UART_Receive_IT(&huart1, &recv_data, 1);
    }
}

// -------------------------- 环形缓冲区 --------------------------
//因为解析内容比较简单，而且串口数据发送量较少且相对集中，故直接在此处理数据
void UART_RingBuffer_Write(uint8_t *ring_buf, uint8_t data)
{
	if(data==UART_FRAME_HEAD)//接收到帧头，接下来开始准备接收数据
	{
		recv_flag=1;
	}else if(data==UART_FRAME_TAIL1)
	{
		tail_index++;
		if(tail_index==3)
		{
			dataupdate();
			tail_index=0;
			recv_index=0;//接收完成
		}
	}else if(recv_flag)//优先判断是否是帧尾，若不是则为数据
	{
		uart1_recv_frame[recv_index++]=data;
	}
	
	
}
